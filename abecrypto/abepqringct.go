// // abecrypto -> abepqringct -> pqringct
// pqringctCryptoAddressGen() generates cryptoAddress, cryptoSpsk, cryptoSnsk, cryptoVsk, by calling pqringct's key-generation functions and encapsed the keys.
// cryptoScheme is set as a parameter, since the map between CryptoScheme and the real crypto-scheme (here is pqringct) is coded by abecryptoparam.
func pqringctCryptoAddressGen(pp *pqringct.PublicParameter, seed []byte,
	cryptoScheme abecryptoparam.CryptoScheme) (
	cryptoAddress []byte,
	cryptoSpsk []byte,
	cryptoSnsk []byte,
	cryptoVsk []byte,
	err error) {
	expectedSeedLen := pqringct.GetParamSeedBytesLen(pp)
	if 2*expectedSeedLen != len(seed) {
		return nil, nil, nil, nil, errors.New("invalid length of seed in pqringctCryptoAddressGen")
	}

	serializedAPk, serializedASksp, serializedASksn, err := pqringct.AddressKeyGen(pp, seed[:expectedSeedLen])
	if err != nil {
		return nil, nil, nil, nil, err
	}

	serializedVPk, serializedVSk, err := pqringct.ValueKeyGen(pp, seed[expectedSeedLen:])
	if err != nil {
		return nil, nil, nil, nil, err
	}

	serializedAddress := append(serializedAPk, serializedVPk...)

	serializeCryptoScheme := cryptoScheme.Serialize()

	cryptoAddress = make([]byte, 0, 4+len(serializedAddress))
	cryptoAddress = append(cryptoAddress, serializeCryptoScheme...)
	cryptoAddress = append(cryptoAddress, serializedAddress...)

	cryptoSpsk = make([]byte, 0, 4+len(serializedASksp))
	cryptoSpsk = append(cryptoSpsk, serializeCryptoScheme...)
	cryptoSpsk = append(cryptoSpsk, serializedASksp...)

	cryptoSnsk = make([]byte, 0, 4+len(serializedASksn))
	cryptoSnsk = append(cryptoSnsk, serializeCryptoScheme...)
	cryptoSnsk = append(cryptoSnsk, serializedASksn...)

	cryptoVsk = make([]byte, 0, 4+len(serializedVSk))
	cryptoVsk = append(cryptoVsk, serializeCryptoScheme...)
	cryptoVsk = append(cryptoVsk, serializedVSk...)

	return cryptoAddress, cryptoSpsk, cryptoSnsk, cryptoVsk, nil
}

/*
  - pqringctCryptoAddressKeySeedGen generates a CryptoAddressKeySeed for PQRingCT scheme.
    The returned pqringctCryptoAddressKeySeed consists of  cryptoScheme (4bytes) + AddressKeySeed for PQRingCT.

Note that with this design, the function here does not need to care what cryptoScheme name is assigned to PQRingCT,
and only needs to use the assigned parameter.
*/
func pqringctCryptoAddressKeySeedGen(pp *pqringct.PublicParameter, cryptoScheme abecryptoparam.CryptoScheme) (
	pqringctCryptoAddressKeySeed []byte) {
	//	For PQRingCT, the CryptoAddressKeySeed need contain two seeds, one for address part and one for value part.
	expectedSeedLen := 2 * pqringct.GetParamSeedBytesLen(pp)
	seed := RandomBytes(expectedSeedLen)

	serializeCryptoScheme := cryptoScheme.Serialize()
	pqringctCryptoAddressKeySeed = make([]byte, 0, 4+expectedSeedLen)
	pqringctCryptoAddressKeySeed = append(pqringctCryptoAddressKeySeed, serializeCryptoScheme...)
	pqringctCryptoAddressKeySeed = append(pqringctCryptoAddressKeySeed, seed...)
	return pqringctCryptoAddressKeySeed
}

/*
* pqringctCryptoAddressGenFromSeed generates address and key for PQRingCT from
cryptoAddressKeySeed which is expected to be generated by pqringctCryptoAddressKeySeedGen.
In particular, cryptoAddressKeySeed = cryptoScheme (4 bytes) + AddressKeySeed for PQRingCT.
pqringctCryptoAddressGen, which takes input pure AddressKeySeed for PQRingCT, will be depreciated.
*/
func pqringctCryptoAddressGenFromSeed(pp *pqringct.PublicParameter, cryptoAddressKeySeed []byte,
	cryptoScheme abecryptoparam.CryptoScheme) (
	cryptoAddress []byte,
	cryptoSpsk []byte,
	cryptoSnsk []byte,
	cryptoVsk []byte,
	err error) {
	expectedSeedLen := pqringct.GetParamSeedBytesLen(pp)
	if 2*expectedSeedLen+4 != len(cryptoAddressKeySeed) {
		return nil, nil, nil, nil, errors.New("invalid length of seed in pqringctCryptoAddressGenFromSeed")
	}

	cryptoschemeInSeed, err := ExtractCryptoSchemeFromCryptoAddressKeySeed(cryptoAddressKeySeed)
	if err != nil || cryptoschemeInSeed != cryptoScheme {
		return nil, nil, nil, nil, errors.New("unmatched cryptoScheme for cryptoAddressKeySeed and pqringctCryptoAddressGenFromSeed")
	}

	serializedAPk, serializedASksp, serializedASksn, err := pqringct.AddressKeyGen(pp, cryptoAddressKeySeed[4:4+expectedSeedLen])
	if err != nil {
		return nil, nil, nil, nil, err
	}

	serializedVPk, serializedVSk, err := pqringct.ValueKeyGen(pp, cryptoAddressKeySeed[4+expectedSeedLen:])
	if err != nil {
		return nil, nil, nil, nil, err
	}

	serializedAddress := append(serializedAPk, serializedVPk...)

	serializeCryptoScheme := cryptoScheme.Serialize()
	cryptoAddress = make([]byte, 0, 4+len(serializedAddress))
	cryptoAddress = append(cryptoAddress, serializeCryptoScheme...)
	cryptoAddress = append(cryptoAddress, serializedAddress...)

	cryptoSpsk = make([]byte, 0, 4+len(serializedASksp))
	cryptoSpsk = append(cryptoSpsk, serializeCryptoScheme...)
	cryptoSpsk = append(cryptoSpsk, serializedASksp...)

	cryptoSnsk = make([]byte, 0, 4+len(serializedASksn))
	cryptoSnsk = append(cryptoSnsk, serializeCryptoScheme...)
	cryptoSnsk = append(cryptoSnsk, serializedASksn...)

	cryptoVsk = make([]byte, 0, 4+len(serializedVSk))
	cryptoVsk = append(cryptoVsk, serializeCryptoScheme...)
	cryptoVsk = append(cryptoVsk, serializedVSk...)

	return cryptoAddress, cryptoSpsk, cryptoSnsk, cryptoVsk, nil
}

func pqringctCryptoAddressSize(pp *pqringct.PublicParameter) uint32 {
	return uint32(4 + pqringct.GetAddressPublicKeySerializeSize(pp) + pqringct.GetValuePublicKeySerializeSize(pp))
}

func pqringctCheckCryptoAddress(pp *pqringct.PublicParameter, cryptoScheme abecryptoparam.CryptoScheme, cryptoAddress []byte) (valid bool, hints string) {
	if len(cryptoAddress) < 4 {
		hints := fmt.Sprintf("pqringctCheckCryptoAddress: invalid length of cryptoAddress: %d", len(cryptoAddress))
		return false, hints
	}

	cryptoscheme, err := abecryptoparam.ExtractCryptoSchemeFromCryptoAddress(cryptoAddress)
	if err != nil || cryptoscheme != cryptoScheme {
		hints := fmt.Sprintf("pqringctCheckCryptoAddress: invalid length of cryptoAddress: %d vs %d", abecryptoparam.CryptoScheme(cryptoscheme), cryptoScheme)
		return false, hints
	}

	expectedCryptoAddressSize := pqringctCryptoAddressSize(pp)
	if uint32(len(cryptoAddress)) != expectedCryptoAddressSize {
		hints := fmt.Sprintf("pqringctCheckCryptoAddress: invalid length of cryptoAddress: %d vs expected %d", len(cryptoAddress), expectedCryptoAddressSize)
		return false, hints
	}

	return true, ""
}

func pqringctVerifyCryptoAddressSpsnsk(pp *pqringct.PublicParameter, cryptoScheme abecryptoparam.CryptoScheme, cryptoAddress []byte, cryptoSpsk []byte, cryptoSnsk []byte) (valid bool, hints string) {

	//	check CryptoAddress
	if validTmp, hintsTmp := pqringctCheckCryptoAddress(pp, cryptoScheme, cryptoAddress); !validTmp {
		return false, hintsTmp
	}

	//	check the cryptoScheme in Spsk and Snsk.
	//	Note that pqringctCheckCryptoAddress has check that the cryptoScheme in cryptoAddress matches the input cryptoScheme.
	if len(cryptoSpsk) < 4 {
		hints := fmt.Sprintf("pqringctVerifyCryptoAddressSpsnsk: invalid length of cryptoSpsk: %d", len(cryptoSpsk))
		return false, hints
	}
	cryptoSchemeInSpsk, err := abecryptoparam.ExtractCryptoSchemeFromCryptoSpsk(cryptoSpsk)
	if err != nil || cryptoSchemeInSpsk != cryptoScheme {
		hints := fmt.Sprintf("pqringctVerifyCryptoAddressSpsnsk: unmacthed cryptoScheme in cryptoSpsk and cryptoAddress: %d vs %d", abecryptoparam.CryptoScheme(cryptoSchemeInSpsk), cryptoScheme)
		return false, hints
	}

	if len(cryptoSnsk) < 4 {
		hints := fmt.Sprintf("pqringctVerifyCryptoAddressSpsnsk: invalid length of cryptoSnsk: %d", len(cryptoSnsk))
		return false, hints
	}

	cryptoSchemeInSnsk, err := abecryptoparam.ExtractCryptoSchemeFromCryptoSnsk(cryptoSnsk)
	if err != nil || cryptoSchemeInSnsk != cryptoScheme {
		hints := fmt.Sprintf("pqringctVerifyCryptoAddressSpsnsk: unmacthed cryptoScheme in cryptoSpsk and cryptoAddress: %d vs %d", abecryptoparam.CryptoScheme(cryptoSchemeInSnsk), cryptoScheme)
		return false, hints
	}

	// extract underlying keys and verify them
	apkLen := pqringct.GetAddressPublicKeySerializeSize(pp)
	serializedAPk := make([]byte, apkLen)
	copy(serializedAPk, cryptoAddress[4:4+apkLen])

	serializedASksp := make([]byte, len(cryptoSpsk)-4)
	copy(serializedASksp, cryptoSpsk[4:])

	serializedASksn := make([]byte, len(cryptoSnsk)-4)
	copy(serializedASksn, cryptoSnsk[4:])

	return pqringct.AddressKeyVerify(pp, serializedAPk, serializedASksp, serializedASksn)

}

func pqringctVerifyCryptoAddressVsk(pp *pqringct.PublicParameter, cryptoScheme abecryptoparam.CryptoScheme, cryptoAddress []byte, cryptoVsk []byte) (valid bool, hints string) {

	//	check CryptoAddress
	if validTmp, hintsTmp := pqringctCheckCryptoAddress(pp, cryptoScheme, cryptoAddress); !validTmp {
		return false, hintsTmp
	}

	//	check the cryptoScheme in cryptoVsk.
	//	Note that pqringctCheckCryptoAddress has check that the cryptoScheme in cryptoAddress matches the input cryptoScheme.
	if len(cryptoVsk) < 4 {
		hints := fmt.Sprintf("pqringctVerifyCryptoAddressVsk: invalid length of cryptoVsk: %d", len(cryptoVsk))
		return false, hints
	}

	cryptoSchemeInVsk, err := abecryptoparam.ExtractCryptoSchemeFromCryptoVsk(cryptoVsk)
	if err != nil || cryptoSchemeInVsk != cryptoScheme {
		hints := fmt.Sprintf("pqringctVerifyCryptoAddressVsk: unmacthed cryptoScheme in cryptoVsk and cryptoAddress: %d vs %d", abecryptoparam.CryptoScheme(cryptoSchemeInVsk), cryptoScheme)
		return false, hints
	}

	// extract underlying keys and verify them
	apkLen := pqringct.GetAddressPublicKeySerializeSize(pp)
	serializedVPk := make([]byte, len(cryptoAddress)-(4+apkLen))
	copy(serializedVPk, cryptoAddress[4+apkLen:])

	serializedVsk := make([]byte, len(cryptoVsk)-4)
	copy(serializedVsk, cryptoVsk[4:])

	return pqringct.ValueKeyVerify(pp, serializedVPk, serializedVsk)

}

