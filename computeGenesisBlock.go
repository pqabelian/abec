var MPK salrs.MasterPubKey
var MSVK salrs.MasterSecretViewKey
var MSSK salrs.MasterSecretSignKey
var DPKs []*salrs.DerivedPubKey

func init() {
	DPKs=make([]*salrs.DerivedPubKey,3)
	salrs.Setup()
	MPK, MSVK, MSSK, Mseed, err := salrs.GenerateMasterKey([]byte{'a', 'b', 'c'})
	if err != nil {
		log.Fatalf("generate master key err = ", err)
	}
	fmt.Printf("MPK = %v\n", MPK)
	fmt.Printf("MPK = %v\n", MPK.Serialize())
	fmt.Printf("MSVK = %v\n", MSVK)
	fmt.Printf("MSSK, = %v\n", MSSK)
	fmt.Printf("Mseed, = %v\n", Mseed)
	DPKs[0], err = salrs.GenerateDerivedPubKey(MPK)
	if err != nil {
		log.Fatalf("generate derived key err = ", err)
	}
	fmt.Printf("DPK[0] = %v\n", DPKs[0])
	fmt.Printf("DPK[0] = %v\n", DPKs[0].Serialize())
}
